<h1>Mean sea surface</h1>

<p>
	The sea level anomaly (SLA) is expressed as the difference of the instantaneous tide-corrected sea
	surface with respect to a well-established mean. Over the years several (more or less) global mean
	sea surface models have been developed from the compilation of satellite altime- ter (and
	sometimes gravity) data. Generally, the more altimeter data collected, the more precise the model.
	But also the resolution of the model counts. Each model has been referenced to the TOPEX reference
	ellipsoid, just as the satellite orbits (Section 2.3).
</p>
