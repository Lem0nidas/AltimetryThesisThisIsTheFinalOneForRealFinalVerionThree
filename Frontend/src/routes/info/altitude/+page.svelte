<h1>Orbital altitude</h1>

<p>
	The orbital altitude is the height of the centre-of-mass of the satellite above the TOPEX
	reference ellipsoid (semi-major axis = 6378136.3m, inverse flattening = 298.257) as computed by
	satellite orbit determination. The altitude is that of the centre-of-mass of the spacecraft, so
	corrections from the tracking devices (<a
		href="https://en.wikipedia.org/wiki/DORIS_(satellite_system)"
		target="_blank"
		rel="noopener noreferrer"
		class="link">DORIS</a
	>,
	<a
		href="https://en.wikipedia.org/wiki/Global_Positioning_System"
		target="_blank"
		rel="noopener noreferrer"
		class="link">GPS</a
	>,
	<a
		href="https://earth.esa.int/eogateway/instruments/prare/description"
		target="_blank"
		rel="noopener noreferrer"
		class="link">PRARE</a
	>,
	<a
		href="https://en.wikipedia.org/wiki/Satellite_laser_ranging"
		target="_blank"
		rel="noopener noreferrer"
		class="link">SLR</a
	>) to the centre-of-mass, as well as motion of the centre-of-mass within the spacecraft are
	accounted for, and should also be accounted for when later subtracting the altimeter range
	referenced to the same point.
</p>
