<h1>Orbital altitude</h1>

<p>
	The orbital altitude is the height of the centre-of-mass of the satellite above the TOPEX ref-
	erence ellipsoid (semi-major axis = 6378136.3 m, inverse flattening = 298.257) as computed by
	satellite orbit determination. The altitude is that of the centre-of-mass of the spacecraft, so
	corrections from the tracking devices (DORIS, GPS, PRARE, SLR) to the centre-of-mass, as well as
	motion of the centre-of- mass within the spacecraft are accounted for, and should also be
	accounted for when later subtracting the altimeter range referenced to the same point.
</p>
