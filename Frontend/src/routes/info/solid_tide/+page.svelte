<h1>Solid earth tide</h1>

<p>
	The solid earth tide is the variation of the elevation of the crust of the earth surface as a
	result of the attraction by the sun and moon (other planets are generally ignored as their
	influence is at least an order of magnitude smaller). As per geodetic convention the ”permanent
	tide” (the mean tide resulting from the mere presence of the sun and moon) is excluded from the
	solid earth tide but is included in the geoid. The RADS implementation of the
	Cartwright-Taylor-Edden solid earth tide model includes 386 second order waves and 99 third order
	waves [Cartwright and Taylor, 1971; Cartwright and Edden, 1973].
</p>
