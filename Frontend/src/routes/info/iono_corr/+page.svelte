<h1>Ioonospheric correction</h1>

<p>
	The radar signal is also delayed by ions and electrons in the upper layers of the atmosphere (the
	ionosphere). The delay is inversely proportional to the altimeter radar frequency, and otherwise
	proportional to the vertically integrated electron density, known as total electron content or
	TEC. This dispersive nature allows dual-frequency altimeters (TOPEX, Envisat, Jason-1 and -2) to
	directly determine the ionospheric path delay on either frequency. For sin- gle frequency
	altimeters we rely on TEC estimates from models based on other dual-frequency equipment (GPS,
	DORIS) or climatologies.
</p>
