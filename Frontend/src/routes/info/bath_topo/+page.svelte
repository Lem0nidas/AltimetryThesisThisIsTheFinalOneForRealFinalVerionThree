<h1>Bathymetry & Topography</h1>

<p>
	Bathymetry is the depth of the oceans and seas. It is given as a negative number, and thus
	constitutes the elevation of the sea bottom with respect to the geoid. The bathymetry is generally
	predicted from altimeter data, by inverting altimeter-derived gravity anomalies into ocean depth
	[Smith and Sandwell, 1994, e.g.].
	<br />
	<br />
	Topography is the elevation of the land and lakes. It is represented generally by a positive value
	and is measured relative to the geoid. Occasionally the values can be negative, like in large parts
	of The Netherlands, and around the Dead Sea. By convention, the elevation of the lake surfaces (not
	the lake bottom) is stored, except for the Caspian Sea for which generally the bottom topography is
	given. The topography models are based on a number of different sources: altimetry, the SRTM mission,
	and local leveling.
</p>
