<h1>Significant wave height</h1>

<p>
	The significant wave height (SWH) is generally defined as the mean wave height (peak to trough) of
	the highest one-third of the ocean waves. Another commonly used definition is four times the
	standard deviation of the elevation of the sea surface in the radar footprint. In MLE3 and MLE4
	retrackers, the SWH is determined from the rate of increase of returned power of the radar
	altimeter pulse (the waveform slope) and requires no further correction other than some instrument
	parameters.
</p>
