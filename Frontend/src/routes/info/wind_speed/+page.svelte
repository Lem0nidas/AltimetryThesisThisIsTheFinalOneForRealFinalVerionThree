<h1>Wind speed</h1>

<p>
	Wind speed can be derived from the altimeter backscatter coefficient. The larger the backscatter,
	the lower the wind speed. Several models have been developed to map this relationship, some
	depending merely on backscatter, some also taking into account significant wave height.
	Three-channel radiometers provide the opportunity to estimate wind speed. In essences this is the
	reverse side of the fact that one of those channels can be replaced with the altimeter wind speed
	to obtain the wet tropospheric correction. This variable is available for all missions with
	3-channel radiometers. Atmospheric models, like those at ECMWF also provide wind speed and wind
	directions, or their vectorial components pointing north and east. Those model values are provided
	for some missions as well.
</p>
