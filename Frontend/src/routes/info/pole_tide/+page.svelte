<h1>Pole tide</h1>

<p>
	The pole tide is the vertical deformation of the earth crust as a result of polar motion. We can
	visualise this as the ellipsoidal shape of the earth being moved as the rotation axis of the earth
	moves away or closer to the mean pole [Munk and MacDonald, 1960]. We use the IERS(EOP)05C04 earth
	orientation parameters and their predictions. The motion of the mean pole, describing the effect
	of global isostatic adjustment (GIA) is represented by a linear mo- tion, as suggested by Wahr et
	al. [2015] following the work of Argus and Gross [2004]. The implicit effect on pole tide is
	further discussed by Desai et al. [2015].
</p>
