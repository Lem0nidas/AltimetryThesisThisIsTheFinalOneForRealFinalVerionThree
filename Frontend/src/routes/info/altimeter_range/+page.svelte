<h1>Altimeter range</h1>

<p>
	The range between the satellite and the sea surface is based on the total travel time of the radar
	pulse divided by twice the speed of light. This range is then corrected for internal paths within
	the instrument (internal calibration), variations in the frequency of the ultra-stable os-
	cillator (USO correction), and the distance between the antenna and the satellite centre of mass
	(centre-of-mass offset). The range is further corrected for the Doppler effect. As such the range
	measures the distance between the satellite centre of mass and the sea surface, except for path
	delays in the atmosphere and sea surface interactions. To compute the height of the sea surface,
	we subtract the primary range (generally Ku-band) from the satellite orbital altitude and then
	further correct for path delays and other corrections. Dual-frequency altimeters measure the range
	also on a secondary frequency (C- and S-band) which allows for the computation of the ionospheric
	path delay directly for altimeter observa- tions rather than models.
</p>
